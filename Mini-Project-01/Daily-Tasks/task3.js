// --- Task A: The Storage Box Concept ---
let apple;            // Variable Declaration (Creating the box)
apple = 10;           // Value Assignment (Putting data in)
apple = apple + 5;    // Reassigning (Updating the value) -> Result: 15
console.log("Task A - Apple Final Count: " + apple);

// --- Task B: The Accumulator Logic (Fish Example) ---
let fish = 0;         // Initializing with zero
fish = fish + 3;      // Adding 3 (Current: 3)
fish = fish + 7;      // Adding 7 (Current: 10)
console.log("Task B - Total Fish Count: " + fish); // Output: 10

// --- Task C: Variable vs. String (The Golden Rule) ---
let userName = "Ahmed";

console.log("Task C - Outputting the Variable Value:");
console.log(userName);    // Result: Ahmed (Reads what's inside the box)

console.log("Task C - Outputting the Literal String:");
console.log("userName");  // Result: userNa
