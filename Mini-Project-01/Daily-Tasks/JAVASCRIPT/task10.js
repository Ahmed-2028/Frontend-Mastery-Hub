// Store a number inside a string (text format)
let myText = "1000";
// Use the Number() function to convert the string "1000" into a real number 1000
let myNumber = Number(myText);

// Check and print the data type of the new variable (result: number)
console.log(typeof myNumber); 
// Use strict equality (===) to check if both value and type match the number 1000
console.log(myNumber === 1000);
